package org.poo.classesforgame;

public class Coordinatess {
    private int x;
    private int y;

    /**
     *
     * @return x
     */
    public int getX() {
        return x;
    }

    /**
     *
     * @param x
     */
    public void setX(final int x) {
        this.x = x;
    }

    /**
     *
     * @return y
     */
    public int getY() {
        return y;
    }

    /**
     *
     * @param y
     */
    public void setY(final int y) {
        this.y = y;
    }
}
